const localeNames: Record<Locale, string> = {
  en: 'EN',
  fr: 'FR',
  es: 'ES',
}

const I18nContext = createContext<{
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: string) => string
  localeNames: Record<Locale, string>
} | null>(null)

function normalizeLocale(value: string | null | undefined): Locale {
  if (value === 'fr' || value === 'es' || value === 'en') return value
  const browser = typeof navigator !== 'undefined' ? navigator.language.slice(0, 2) : 'en'
  if (browser === 'fr' || browser === 'es') return browser
  return 'en'
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() =>
    normalizeLocale(
      typeof window !== 'undefined'
        ? new URLSearchParams(window.location.search).get('lang') ?? localStorage.getItem('locale')
        : null,
    ),
  )

  useEffect(() => {
    document.documentElement.lang = locale
    localStorage.setItem('locale', locale)
  }, [locale])

  const value = useMemo(() => ({
    locale,
    setLocale: setLocaleState,
    t: (key: string) => dictionaries[locale][key] ?? dictionaries.en[key] ?? key,
    localeNames,
  }), [locale])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const value = useContext(I18nContext)
  if (!value) throw new Error('useI18n must be used inside I18nProvider')
  return value
}
